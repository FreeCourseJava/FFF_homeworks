package fifth;

public class House {
    int number;
    int area;

    public House() {
        this.number = 1;
        this.area = 100;
    }

    public House(int number) {
        this.number = number;
        this.area = 100;
    }

    public House(int number, int area) {
        this.number = number;
        this.area = area;
    }
}
