package fifth;

public class House {
    int number;
    int width;
    int length;


    public House(int number) {
        this.number = number;
        this.length = 100;
        this.width = 10;
    }

    public House(int number, int length, int width) {
        this.number = number;
        this.length = length;
        this.width = width;

    }
}
